// Big O => O(n)
function largestProduct(thousandDigits, n) {
  let size = n;
  let left = 0;
  //   let right = left + n - 1;
  let right = left + n;
  // 先算出乘積
  let product = 1;
  for (let i = 0; i < size; i++) {
    if (thousandDigits[i] === 0) {
      product = 0;
    } else {
      product *= thousandDigits[i];
    }
  }
  console.log(`初始乘積:${product}`);
  let ans = product;
  let need_record_count = 0; // 遇到0的話要去計算
  let need_multiply = 1;
  // 如果遇到0的話代表下面4個都不用看了，一定會為0，而且除以0會出現錯誤，先定義need_record_count=size那就紀錄need_multiply=[]往右移動.append，最後乘起來
  while (right < thousandDigits.length) {
    // 這邊先判斷下一個數是不是0
    // 若是的話代表下面n個數字的乘積都是0
    if (thousandDigits[right] === 0) {
      console.log(`這個window的右邊界會為0=>index:${right}`);
      product = 0;
      // 扣掉當前這個下面n-1次的product也會是0，因為window內都包含0
      need_record_count = size - 1;
      // 後面幾個要先相乘，這樣才能繼續window，並且不用重新往回乘
      need_multiply = 1;
    } else if (need_record_count > 0) {
      console.log(
        `window內還存在0，目前右邊界為${right}，還要再向右${need_record_count}格，0才會離開window`
      );
      // need_record_count如果還大於0的話代表0還在window內
      need_record_count--;
      // 計算0右邊的乘積，如果這個right又是0的話會上一個if會成立，就不會到這裡，並且need_multiply與need_record_count會重置
      need_multiply *= thousandDigits[right];
    } else {
      // need_multiply大於1代表0綱要離開window左邊界
      if (need_multiply > 1) {
        console.log(
          `這個window右邊界index為:${right}，上個window左邊界為${left} value:${thousandDigits[left]}，0剛離開window`
        );
        // need_multiply為window內有0的右邊幾個數的乘積，乘上現在這個right，就不用去除以left了，因為數字/0會為NaN
        product = need_multiply * thousandDigits[right];
        // need_multiply重置為1
        need_multiply = 1;
      } else {
        // 除以上個window的左邊界
        product /= thousandDigits[left];
        // 乘以這個window的右邊界
        product *= thousandDigits[right];
      }
    }
    if (product > ans) {
      ans = product;
      console.log(
        `======== 目前${
          left + 1
        }~${right}這個window的乘積是最大的，乘積為:${ans} ========`
      );
    }
    // 不管如何right與left都往右一格，然後比較product是否大於ans，若window內存在0的話product會為0
    left++;
    right++;
  }
  return ans;
}
